package ch06.example;

public class Printer {
	
	
	public static void println(int p) {
		System.out.println(p);
	}
	public static void println(boolean p) {
		System.out.println(p);
	}
	public static void println(double p) {
		System.out.println(p);
	}
	public static void println(String p) {
		System.out.println(p);
	}
}
